console.log(foo);  // 输出函数体，因为函数声明提升
var foo = 1;
function foo() {}
console.log(foo);  // 1（变量赋值覆盖了函数）
// 同一个作用域内，函数声明优先于变量声明。
// 第二个console.log是因为变量后声明，所以覆盖前面的函数foo

